import math

numero = int(input("Digite um numero: "))

dobro =  numero * 2
quadrado = numero **2
cubo = numero **3
raiz = math.sqrt(numero)

print("O dobro do numero é: ",dobro)
print("O numero elevado ao quadrado é: ",quadrado)
print("O numero elevado ao cubo é: ",cubo)
print("A raiz quadrada do numero é: ",raiz)
